package it.cnr.ilc.cophi.action.userbean;

public class SubAnalysisBean {

	private String id;
	private String pos;
	private String post;
	private String posf;
	private String word;
	private String wordt;
	private String wordf;
	
	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}
	/**
	 * @return the pos
	 */
	public String getPos() {
		return pos;
	}
	/**
	 * @param pos the pos to set
	 */
	public void setPos(String pos) {
		this.pos = pos;
	}
	/**
	 * @return the post
	 */
	public String getPost() {
		return post;
	}
	/**
	 * @param post the post to set
	 */
	public void setPost(String post) {
		this.post = post;
	}
	/**
	 * @return the word
	 */
	public String getWord() {
		return word;
	}
	/**
	 * @param word the word to set
	 */
	public void setWord(String word) {
		this.word = word;
	}
	/**
	 * @return the wordt
	 */
	public String getWordt() {
		return wordt;
	}
	/**
	 * @param wordt the wordt to set
	 */
	public void setWordt(String wordt) {
		this.wordt = wordt;
	}
	/**
	 * @return the posf
	 */
	public String getPosf() {
		return posf;
	}
	/**
	 * @param posf the posf to set
	 */
	public void setPosf(String posf) {
		this.posf = posf;
	}
	/**
	 * @return the wordf
	 */
	public String getWordf() {
		return wordf;
	}
	/**
	 * @param wordf the wordf to set
	 */
	public void setWordf(String wordf) {
		this.wordf = wordf;
	}


}
